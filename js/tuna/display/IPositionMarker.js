/////////////////////////////////////////////////////////////////////////////////////////

/**
 * Интерфейс класса служащего для пометки объектов отображения, позиции которых необходимо
 * сохранить после обновления.
 */
pkg.IPositionMarker = function() {};

/**
 * Инициализация поведения элементоы отображения.
 *
 * @param {String} containerID Индектификатор контейнера в котором будет происходить
 * обновление.
 * @return {Array.<String>} Набор идентификаторв элементов сохраняющих позиции.
 */
pkg.IPositionMarker.prototype.getMarkerNodes = function(containerID)
{
	throw new Error("Метод getMarkerNodes(containerID):Array.<String> интерфейса " +
		"tuna.display.IPositionMarker должен быть реализован.");

	return null;
};

/////////////////////////////////////////////////////////////////////////////////////////
//	Сокращения
/////////////////////////////////////////////////////////////////////////////////////////

tuna.typedef(pkg.IPositionMarker, "IPositionMarker");
