tuna.include("tuna.transform.ITransformer");
tuna.include("tuna.ya.YaDomain");

/////////////////////////////////////////////////////////////////////////////////////////
//	Область имен.
/////////////////////////////////////////////////////////////////////////////////////////

var pkg = tuna.namespace("tuna.ya");

/////////////////////////////////////////////////////////////////////////////////////////
//
//	Класс tuna.ya.YaTransformer
//
/////////////////////////////////////////////////////////////////////////////////////////

/**
 * Класс ленивой трансформации данных для проекта "YaTalant.ru".
 *
 * @public
 * @constructor
 * @param {String} id Идентификатор контейнера для трансформации.
 */
pkg.YaTransformer = function(id)
{
	/**
	 * Контейнер динамической трансформации.
	 *
	 * @private
	 * @type {tuna.ya.YaDomain}
	 */
	this.__container = new tuna.ya.YaDomain(id);
};

tuna.implement(pkg.YaTransformer, tuna.transform.ITransformer);

/**
 * Проведение трансформации.
 *
 * Трансформация происходит "лениво", то есть перестраиваются только элементы, зависящие
 * от изменившихся данных.
 *
 * В связи с этим результату не нужно быть описанием итогового представления, и поэтому
 * результатом всегда будет <code>null</code>.
 *
 * @param {Object} data Данные трансформации в виде объекта.
 * @return {Object} Всегда <code>null</code>.
 */
pkg.YaTransformer.prototype.transform = function(data)
{
	this.__container.renderData(data);

	return null;
}

/////////////////////////////////////////////////////////////////////////////////////////
//	Сокращения
/////////////////////////////////////////////////////////////////////////////////////////

tuna.typedef(pkg.YaTransformer, "YaTransformer");