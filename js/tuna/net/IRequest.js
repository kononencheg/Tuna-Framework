tuna.include("tuna.event.IEventDispatcher");

/////////////////////////////////////////////////////////////////////////////////////////
//	Область имен.
/////////////////////////////////////////////////////////////////////////////////////////

var pkg = tuna.namespace("tuna.net");

/////////////////////////////////////////////////////////////////////////////////////////
//
//	Интерфейс tuna.net.IRequest
//
/////////////////////////////////////////////////////////////////////////////////////////

/**
 * Интерфейс объектов запроса удаленного сервиса.
 *
 * @extend tuna.event.IEventDispatcher
 */
pkg.IRequest = function() {};

/////////////////////////////////////////////////////////////////////////////////////////
//	Наседование интрефейса tuna.event.IEventDispatcher
/////////////////////////////////////////////////////////////////////////////////////////

tuna.extend(pkg.IRequest, tuna.event.IEventDispatcher);

/**
 * Функция передачи данных.
 *
 * @param {String} url URL Удаленного сервиса.
 */
pkg.IRequest.prototype.send = function(url)
{
	throw new Error("Метод send(url:String):void интерфейса tuna.net.IRequest должен" +
		" быть реализован");
};

/**
 * Обрыв передачи данных.
 */
pkg.IRequest.prototype.abort = function()
{
	throw new Error("Метод abort():void интерфейса tuna.net.IRequest должен" +
		" быть реализован");
};

/////////////////////////////////////////////////////////////////////////////////////////
//	Сокращения
/////////////////////////////////////////////////////////////////////////////////////////

tuna.typedef(pkg.IRequest, "IRequest");
